import { SocketEvent, SocketId } from "./types/socket"
import { USER_CONNECTION_STATUS, User } from "./types/user"
import { Server } from "socket.io"
import path from "path"

// he path module in Node.js provides a way to work with file and directory paths, making it easier to navigate, handle, and manipulate them across various operating systems. Let’s break down why and how it’s used in detail.

